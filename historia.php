<h2>HISTORIA</h2>

<h3>Idea</h3>
<p>Alkuperäinen idea on kopioitu yhdysvaltalaiselta 
<a href="http://www.blogsweatandbeers.com/">The Outdoorsmen</a> äijäporukalta, jotka ovat jo vuosien ajan kokoontuneet
jonnekin metsään kisailemaan oluen voimin. Ainakin sinä viikonloppuna päästään karkuun hirmuisia vaimoja ja kaupungien
stressiä. He ovat myös filmanneet dokumentin, <i>Blog, Sweat & Beers</i>, yhdestä tapahtumastaan. Tisk sattui näkemään
sen ja innostuneena esitteli konseptin Abulalle, joka myös oli vakuuttunut tapahtuman hauskuudesta. Tästä alkoi oman
porukan kasaaminen ja ensimmäisen <i>Ulko-ovimiehet</i> -tapahtuman järjestely.</p>


<h3>2007</h3>
<p>Ensimmäinen Ulko-ovimiehet -tapahtuma järjestettiin 22.9.2007 Merikarvialla Tiskin mökillä. Miehiä oli kokoontunut
paikalla kuusi: Abula, Tisk, Hermu, AndyH, vender ja Orcc. Linnanmaan liikuntoilijat (Abula, Hermu) veivät ylivoimaisen
voiton tällä kertaa ja seuraavina olivat All-in (Tisk, AndyH) ja Voittajat (vender, Orcc).</p>

<p><i>Koska kaikilla oli niin muukaavaa...</i> niin perinnettä tullaan varmasti jatkamaan myös ensi kesänä. Sitten
osaamme paremmin valmistautua koitokseen ja esimerkiksi päivämäärä tullaan lyömään lukkoon aikaisemmassa vaiheessa.
Myös lajilistaa ja oluiden sijoittamista lajeihin tullaan tarkistamaan.</p>


