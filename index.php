<h2>ULKO-OVIMIEHET</h2>

<h3>Esittely</h3>
<p>Ulko-ovimiehet on suomalainen versio <a href="http://www.blogsweatandbeers.com/">The Outdoorsmen</a> äijäporukasta,
joka järjestää vuosittain keskenään mökkiviikonlopun, missä keräännytään ensisijaisesti juomaan olutta, toissijaisesti
urheilemaan ja viimeisimmäksi yritetään pitää vielä hauskaa. Pääasiassa liikunnalliset lajit suoritetaan oluenjuonnin 
siivittämänä. Lajeista jaetaan pisteitä ja päivän lopuksi kruunataan voittaja.</p>

